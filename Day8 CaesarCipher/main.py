while again == "yes":
    enc = input("Type 'encode' to encode type 'decode' to decode: ")
    enc = enc.lower()
    msg = input("Type your message: ")
    msg = msg.lower()

    shift = int(input("Type the shift number: "))
    if shift > 26:
        shift = shift % 26

    encoded_msg = ""
    decoded_msg = ""
    if enc == "encode":
        for letter in msg:
            if ord(letter) >= 97 and ord(letter) <= 122:
                letter = chr(ord(letter) + shift)
                encoded_msg = encoded_msg + letter
            else:
                encoded_msg = encoded_msg + letter
                continue
        print(encoded_msg)
    elif enc == "decode":
        for letter in msg:
            if ord(letter) >= 97 and ord(letter) <= 122:
                letter = chr(ord(letter) - shift)
                decoded_msg = decoded_msg + letter
            else:
                decoded_msg = decoded_msg + letter
                continue
        print(decoded_msg)

    again = input("Type 'yes' if you want to go again. Otherwise type 'no': ")
