while again=='y':
    if(count>=2):
        num1=result    
    for op in calci:
        print("'"+op+"'",end=", ")
    print()
    operator = input("Pick the operator: ")
    num2 = float(input("Enter the next number: "))
    function = calci[operator]
    result = function(num1,num2)
    print(str(num1)+" "+operator+" "+str(num2)+" = "+str(result))
    again = input("Enter 'y' for next operation or enter 'n' for ending: " )
    count+=1
